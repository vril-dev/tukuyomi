export const I18nContext = createContext<I18nContextValue | null>(null);

let activeLocale: Locale = DEFAULT_LOCALE;

function normalizeLocale(value: string | null | undefined): Locale {
  return value === "ja" ? "ja" : DEFAULT_LOCALE;
}

export function detectInitialLocale(): Locale {
  if (typeof globalThis.localStorage !== "undefined") {
    const stored = globalThis.localStorage.getItem(STORAGE_KEY);
    if (stored) {
      return normalizeLocale(stored);
    }
  }
  if (typeof globalThis.navigator !== "undefined" && globalThis.navigator.language.toLowerCase().startsWith("ja")) {
    return "ja";
  }
  return DEFAULT_LOCALE;
}

function interpolate(template: string, vars?: TranslationVars) {
  if (!vars) {
    return template;
  }
  return template.replace(/\{([^}]+)\}/g, (_, key: string) => {
    const value = vars[key];
    return value == null ? "" : String(value);
  });
}

export function translate(locale: Locale, key: string, vars?: TranslationVars): string {
  const template = locale === "ja" ? JA_STRINGS[key] ?? key : key;
  return interpolate(template, vars);
}

export function translateCurrent(key: string, vars?: TranslationVars): string {
  return translate(activeLocale, key, vars);
}

export function getCurrentLocale(): Locale {
  return activeLocale;
}

export function setCurrentLocale(locale: Locale) {
  activeLocale = locale;
}

export function useI18n() {
  const value = useContext(I18nContext);
  if (!value) {
    throw new Error("useI18n must be used within I18nProvider");
  }
  return value;
}
